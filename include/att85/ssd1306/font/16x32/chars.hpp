template<typename Display, typename Font, char c>
struct send_char;

#define ATT85_SSD1306_SPEC_SEND_CHAR_16x32(T, C)                        \
template<typename Display>                                              \
struct send_char<Display, font::_16x32, C> {                            \
    using Sda = typename Display::Sda;                                  \
    using Scl = typename Display::Scl;                                  \
    constexpr static void send() {                                      \
        detail::send_char<                                              \
           Sda,                                                         \
           Scl,                                                         \
           font::_16x32>(font::_16x32::T ##_## gddram);                 \
    }                                                                   \
};

